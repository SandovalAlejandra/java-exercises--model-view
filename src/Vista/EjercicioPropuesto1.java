    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
            String nom = txtNombre.getText();
            int hijos = Integer.parseInt(txtcantH.getText());
            String categoria =cbc.getSelectedItem().toString();
            arrayEmpleados[pos]=new Empleado(nom, hijos, categoria);
            
            jTextArea.setText("");
            for(int i=0;i<=pos;i++){
                jTextArea.append(arrayEmpleados[i].getInformacion()); 
            }        
        
            pos++;
            limpiar_cajas();
    }//GEN-LAST:event_btnCalcularActionPerformed

