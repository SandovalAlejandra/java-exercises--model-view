        String nombre = txtNombre.getText();
        double basico = Double.parseDouble(txtBasico.getText());
        arrayTra[pos] = new Trabajador(nombre, cargo, basico);
        
        txtArea.setText("");
        for(int i=0;i<=pos;i++){
           txtArea.append(arrayTra[i].getInformacion()); 
        }
        
        pos++;
        limpiar_cajas();
